<h1>Single Page Application Vanilla JavaScript</h1>

<p>
  How hard, is to make <b>SPA - Single Page Application</b> using vanilla JavaScript? Well it's not hard it's like 50 lines of code [<b>0.5kb</b>].
</p>

<h1>Basic Concept</h1>

<p>
     Web sites that work like SPA are working like normal web page when JS is not available.<br>
    Java Script will take control over links in main menu and turn them into AJAX calls that will download page content only.
</p>

